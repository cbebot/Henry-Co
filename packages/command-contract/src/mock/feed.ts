/** Build the validated, deterministic mock attention feed. */
export function mockAttentionFeed(): AttentionItem[] {
  return SEEDS.map((seed, index) => {
    const { status, id, ...rest } = seed;
    const input: AttentionItemInput = {
      ...rest,
      id: attentionItemId(id),
      createdAt: createdAtFor(index),
    };
    const result = validateAttentionInput(input);
    if (!result.ok) {
      throw new Error(`mock feed seed "${id}" is invalid — ${result.error.message}`);
    }
    return status ? { ...result.value, status } : result.value;
  });
}
