const LOCALE_OVERRIDES: Partial<Record<AppLocale, Partial<SurfaceCopy>>> = {
  fr: FR,
  es: ES,
  pt: PT,
  ar: AR,
  ig: IG,
  yo: YO,
  ha: HA,
  de: DE,
  it: IT,
  zh: ZH,
  hi: HI,
};

export function getSurfaceCopy(locale: AppLocale): SurfaceCopy {
  const overrides = LOCALE_OVERRIDES[locale];
  if (!overrides) return EN;
  return deepMergeMessages(
    EN as unknown as Record<string, unknown>,
    overrides as unknown as Record<string, unknown>,
  ) as unknown as SurfaceCopy;
}

export function translateSurfaceLabel(locale: AppLocale, label: string): string {
  return getSurfaceCopy(locale).labels[label] ?? label;
}

export function formatSurfaceTemplate(
  template: string,
  values: Record<string, string | number>,
): string {
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  );
}
